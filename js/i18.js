    function changeLanguage(lang) {
    document.querySelectorAll("[data-key]").forEach(el => {
        const key = el.dataset.key;
        if (!translations[lang] || !translations[lang][key]) return;

        if (el.hasAttribute("data-html")) {
            el.innerHTML = translations[lang][key];
        } else {
            el.textContent = translations[lang][key];
        }
    });

    localStorage.setItem("lang", lang);
    updateDropdownText(lang);
    document.documentElement.lang = lang;

    if (window.AOS) {
        AOS.refresh();
    }
}


    langButtons.forEach(btn => {
        btn.addEventListener("click", e => {
            e.preventDefault();
            changeLanguage(btn.dataset.lang);
        });
    });

    /* Location a gore avtomatik dil secimi Start  */

    async function detectUserLang() {
        try {
            const res = await fetch("https://ipapi.co/json/");
            const data = await res.json();
            const country = data.country_code;

            let lang = "en";

            if (country === "AZ") lang = "az";
            else if (country === "RU") lang = "ru";

            const savedLang = localStorage.getItem("lang");
            changeLanguage(savedLang || lang);
        } catch (err) {
            console.error("Country detect error:", err);
            const savedLang = localStorage.getItem("lang");
            changeLanguage(savedLang || "en");
        }
    }

    detectUserLang();

    /* Location a gore avtomatik dil secimi End */

